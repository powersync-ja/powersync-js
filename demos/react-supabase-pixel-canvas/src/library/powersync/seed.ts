import { AbstractPowerSyncDatabase } from '@powersync/web';

import { GRID_SIZE, WHITE_INDEX } from '../palette';
import { pixelId } from './pixels';

const SEED_AUTHOR = 'seed';
const CHUNK_ROWS = 128; // 128 rows * 6 params = 768 bound params per statement.

/**
 * Seed a blank canvas locally so the app works with no backend configured.
 *
 * Only runs in standalone mode. Writes to the synced `pixels` view queue crud
 * (PUT) entries; we delete them in the same transaction so that, if a backend
 * is configured later against this same local database, the seed can never
 * replay upstream and overwrite the live server canvas. This DELETE FROM ps_crud
 * is safe *only* because we are provably standalone (no connector exists, so
 * nothing else is ever queued) — never do this when connected.
 */
export async function seedPixelsIfNeeded(db: AbstractPowerSyncDatabase): Promise<void> {
  await db.writeTransaction(async (tx) => {
    const existing = await tx.get<{ count: number }>(`SELECT COUNT(*) AS count FROM pixels`);
    if (existing.count > 0) {
      return;
    }

    for (let start = 0; start < GRID_SIZE * GRID_SIZE; start += CHUNK_ROWS) {
      const values: string[] = [];
      const params: (string | number)[] = [];
      for (let i = start; i < Math.min(start + CHUNK_ROWS, GRID_SIZE * GRID_SIZE); i++) {
        const x = i % GRID_SIZE;
        const y = Math.floor(i / GRID_SIZE);
        values.push('(?, ?, ?, ?, ?, ?)');
        params.push(pixelId(x, y), x, y, WHITE_INDEX, SEED_AUTHOR, '');
      }
      await tx.execute(
        `INSERT INTO pixels (id, x, y, color, updated_by, updated_at) VALUES ${values.join(', ')}`,
        params
      );
    }

    // Discard the queued PUT entries generated by the seed inserts above.
    await tx.execute(`DELETE FROM ps_crud`);
  });
}
